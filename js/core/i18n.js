export function registerTranslations(entries) {
  for (const lang of SUPPORTED_LANGS) {
    if (!entries[lang]) continue;
    Object.assign(TRANSLATIONS[lang], entries[lang]);
  }
}

export function getLang() {
  return currentLang;
}

export function t(key, vars) {
  const dict = TRANSLATIONS[currentLang] || TRANSLATIONS.fr;
  let text = dict[key] ?? TRANSLATIONS.fr[key] ?? key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      text = text.replaceAll(`{${name}}`, value);
    }
  }
  return text;
}

export function applyStaticTranslations(root = document) {
  root.querySelectorAll("[data-i18n]").forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  root.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  root.querySelectorAll("[data-i18n-title]").forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
  root.querySelectorAll("[data-i18n-html]").forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
}

export function onLanguageChange(callback) {
  listeners.add(callback);
  return () => listeners.delete(callback);
}

export function setLang(lang) {
  if (!SUPPORTED_LANGS.includes(lang) || lang === currentLang) return;
  currentLang = lang;
  try { localStorage.setItem(STORAGE_KEY, lang); } catch { /* stockage indisponible, tant pis */ }
  document.documentElement.lang = lang;
  applyStaticTranslations();
  document.querySelectorAll(".lang-switch-btn").forEach(btn => {
    btn.classList.toggle("active", btn.dataset.lang === currentLang);
  });
  listeners.forEach(callback => callback(lang));
}

export function initI18n() {
  let saved;
  try { saved = localStorage.getItem(STORAGE_KEY); } catch { saved = null; }

  if (saved && SUPPORTED_LANGS.includes(saved)) {
    currentLang = saved;
  } else {
    const browserLang = String(navigator.language || "fr").slice(0, 2).toLowerCase();
    currentLang = SUPPORTED_LANGS.includes(browserLang) ? browserLang : "fr";
  }

  document.documentElement.lang = currentLang;
  applyStaticTranslations();
  wireLanguageSwitcher();
}

const LANG_LABELS = { fr: "FR", en: "EN", zh: "中文" };

export function renderLanguageSwitcherHtml() {
  return SUPPORTED_LANGS.map(lang =>
    `<button type="button" class="lang-switch-btn${lang === currentLang ? " active" : ""}" data-lang="${lang}">${LANG_LABELS[lang]}</button>`
  ).join("");
}

export function wireLanguageSwitcher(root = document) {
  root.querySelectorAll(".lang-switch-btn").forEach(btn => {
    btn.onclick = () => setLang(btn.dataset.lang);
  });
}

// Injecte + câble le switcher dans un conteneur donné. À appeler APRÈS initI18n()
// pour que le bouton actif reflète bien la langue déjà résolue (storage/navigateur).
export function mountLanguageSwitcher(container) {
  if (!container) return;
  container.innerHTML = renderLanguageSwitcherHtml();
  wireLanguageSwitcher(container);
}
